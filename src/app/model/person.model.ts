export class Person {

  name: string;
  lname: string;
  adress: string;
  postcode: string;
  city: string;
  tel: string;


  constructor(name: string, lname: string, adress: string, postcode: string, city: string, tel: string) {
    this.name = name;
    this.lname = lname;
    this.adress = adress;
    this.postcode = postcode;
    this.city = city;
    this.tel = tel;

  }

}
